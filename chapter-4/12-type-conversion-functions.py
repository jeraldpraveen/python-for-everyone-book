converted_value = int('32.0')
print(converted_value)  # Output : 32
converted_value1 = float('32')
print(converted_value1)  # Output : 32.0
converted_value2 = str(100)
print(converted_value2)  # Output : '100'
converted_value3 = bool(1)
print(converted_value3)  # Output : True
converted_value4 = list((1, 2, 3))
print(converted_value4)  # Output : [1, 2, 3]
converted_value5 = tuple([1, 2, 3])
print(converted_value5)  # Output : (1, 2, 3)
converted_value6 = set([1, 2, 2, 3])
print(converted_value6)  # Output : {1, 2, 3}
converted_value7 = dict([('a', 1), ('b', 2)])
print(converted_value7)  # Output : {'a': 1, 'b': 2}
converted_value8 = complex(2, 3)
print(converted_value8)  # Output : (2+3j)
converted_value9 = bytes('hello', 'utf-8')
print(converted_value9)  # Output : b'hello'
