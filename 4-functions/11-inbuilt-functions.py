import math
type_of_value = type(32)
print(type_of_value)  # Output : <class 'int'>

max_value = max(10, 20)
print(max_value)  # Output : 20
min_value = min(10, 20)
print(min_value)  # Output : 10
abs_value = abs(-15)
print(abs_value)  # Output : 15
round_value = round(3.6)
print(round_value)  # Output : 4
pow_value = pow(2, 3)
print(pow_value)  # Output : 8
len_value = len("Hello")
print(len_value)  # Output : 5

# dot notation is used to access functions from modules
sqrt_value = math.sqrt(16)
print(sqrt_value)  # Output : 4.0
