# Assignment statements
width = 17
height = 12.0

# Expressions
expr1 = width // 2
expr2 = width / 2.0
expr3 = height / 3
expr4 = 1 + 2 * 5

# Print height, width and results of expressions
print("Given width =", width, "and height =", height)
print("1. width // 2 =", expr1, "--> // operator results in an integer")
print("2. width / 2.0 =", expr2, "--> / operator results in a float")
print("3. height / 3 =", expr3, "--> / operator results in a float")
print("4. 1 + 2 * 5 =", expr4, "--> follows order of operations")
